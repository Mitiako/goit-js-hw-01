        function getElementWidth(content, padding, border) {
            const contentWidth = parseFloat(content);
            const paddingWidth = parseFloat(padding);
            const borderWidth = parseFloat(border);

                // Ось тут прям довго сумнівався, чи правильно буде додавати paddingWidth та borderWidth по 2 рази, але в кінцевому результаті прийшов до висновку, що так, це правильно, тому що padding та border додаються з обох сторін елемента (зліва і справа).
            return contentWidth + paddingWidth * 2 + borderWidth * 2;
        }

